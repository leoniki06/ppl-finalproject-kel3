<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Counter animation for impact numbers
        const initCounters = () => {
            const counters = document.querySelectorAll('.impact-number');

            const startCounter = (counter) => {
                const target = parseInt(counter.getAttribute('data-count'));
                const increment = target / 100;
                let current = 0;
                let duration = 2000;
                let startTime = null;

                const animateCounter = (timestamp) => {
                    if (!startTime) startTime = timestamp;
                    const progress = timestamp - startTime;
                    const percentage = Math.min(progress / duration, 1);

                    // Ease out function for smooth deceleration
                    const easeOut = 1 - Math.pow(1 - percentage, 3);

                    current = target * easeOut;
                    counter.textContent = Math.floor(current).toLocaleString();

                    if (percentage < 1) {
                        requestAnimationFrame(animateCounter);
                    } else {
                        counter.textContent = target.toLocaleString();
                        counter.style.background =
                            'linear-gradient(135deg, var(--accent-color) 0%, #FFD700 100%)';
                        counter.style.webkitBackgroundClip = 'text';
                        counter.style.webkitTextFillColor = 'transparent';
                    }
                };

                // Start counter when in viewport
                const observer = new IntersectionObserver((entries) => {
                    entries.forEach(entry => {
                        if (entry.isIntersecting) {
                            requestAnimationFrame(animateCounter);
                            observer.unobserve(entry.target);
                        }
                    });
                }, {
                    threshold: 0.5,
                    rootMargin: '0px 0px -100px 0px'
                });

                observer.observe(counter);
            };

            counters.forEach(startCounter);
        };

        // Initialize counters
        initCounters();

        // Update copyright year
        const updateCopyrightYear = () => {
            const currentYear = new Date().getFullYear();
            const yearElement = document.getElementById('currentYear');
            if (yearElement) {
                yearElement.textContent = currentYear;
            }
        };

        updateCopyrightYear();

        // Newsletter form functionality
        const newsletterForm = document.querySelector('.newsletter-form');
        const newsletterInput = document.querySelector('.newsletter-input');
        const newsletterButton = document.querySelector('.newsletter-button');

        if (newsletterButton) {
            newsletterButton.addEventListener('click', function() {
                const email = newsletterInput.value.trim();

                if (!email) {
                    showNotification('Please enter your email address', 'warning');
                    newsletterInput.focus();
                    return;
                }

                if (!isValidEmail(email)) {
                    showNotification('Please enter a valid email address', 'error');
                    newsletterInput.focus();
                    return;
                }

                // Simulate subscription
                showNotification('Thank you for subscribing to our newsletter!', 'success');
                newsletterInput.value = '';
            });

            // Allow Enter key submission
            newsletterInput.addEventListener('keypress', function(e) {
                if (e.key === 'Enter') {
                    newsletterButton.click();
                }
            });
        }

        // Email validation helper
        function isValidEmail(email) {
            const re = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
            return re.test(email);
        }

        // Notification system
        function showNotification(message, type = 'info') {
            const notification = document.createElement('div');
            notification.className = 'footer-notification';
            notification.style.cssText = `
                position: fixed;
                bottom: 30px;
                right: 30px;
                background: ${type === 'error' ? '#dc3545' : type === 'success' ? '#28a745' : type === 'warning' ? '#ffc107' : 'var(--primary-color)'};
                color: white;
                padding: 15px 25px;
                border-radius: 10px;
                box-shadow: 0 6px 20px rgba(0,0,0,0.15);
                z-index: 9999;
                font-family: 'Poppins', sans-serif;
                font-weight: 500;
                max-width: 350px;
                transform: translateX(120%);
                transition: transform 0.4s ease;
                display: flex;
                align-items: center;
                gap: 12px;
            `;

            let icon = 'info-circle';
            if (type === 'success') icon = 'check-circle';
            if (type === 'error') icon = 'exclamation-circle';
            if (type === 'warning') icon = 'exclamation-triangle';

            notification.innerHTML = `
                <i class="fas fa-${icon}" style="font-size: 18px;"></i>
                <span>${message}</span>
            `;

            document.body.appendChild(notification);

            // Animate in
            setTimeout(() => {
                notification.style.transform = 'translateX(0)';
            }, 10);

            // Remove after 3 seconds
            setTimeout(() => {
                notification.style.transform = 'translateX(120%)';
                setTimeout(() => {
                    if (notification.parentNode) {
                        notification.remove();
                    }
                }, 400);
            }, 3000);
        }

        // Add hover effect to impact cards
        const impactCards = document.querySelectorAll('.impact-card');
        impactCards.forEach(card => {
            card.addEventListener('mouseenter', () => {
                card.style.zIndex = '10';
            });

            card.addEventListener('mouseleave', () => {
                card.style.zIndex = '1';
            });
        });

        // Add click effect to social links
        const socialLinks = document.querySelectorAll('.social-link');
        socialLinks.forEach(link => {
            link.addEventListener('click', function(e) {
                e.preventDefault();
                const platform = this.getAttribute('aria-label');
                showNotification(`Opening ${platform}...`, 'info');
                // In production, this would redirect to actual social media links
            });
        });

        // Handle window resize
        let resizeTimeout;
        window.addEventListener('resize', function() {
            clearTimeout(resizeTimeout);
            resizeTimeout = setTimeout(function() {
                console.log('Footer layout adjusted for window resize');
            }, 250);
        });

        console.log('Last Bite Footer initialized successfully');
    });
